let register = async (event) => {
  event.preventDefault();

  try {
    let name = document.getElementById("name");
    name = name.value;
    let email = document.getElementById("email");
    email = email.value;
    let password = document.getElementById("password");
    password = password.value;

    let gender = document.getElementById("gender");
    gender = gender.value;

    let age = document.getElementById("age");
    age = age.value;

    let city = document.getElementById("city");
    city = city.value;

    let data = {
      name,
      email,
      gender,
      password,
      age,
      city,
    };
    let res = await fetch(
      "https://blue-blushing-deer.cyclic.app/users/register",
      {
        method: "POST",
        body: JSON.stringify(data),
        headers: {
          "Content-Type": "application/json",
        },
      }
    );

    let odata = await res.json();
    alert("register successful");
    console.log(odata);
  } catch (error) {
    console.log(error);
    alert(error);
  }
};

let login = async (event) => {
  event.preventDefault();

  try {
    let login_email = document.getElementById("login_email");
    login_email = login_email.value;

    let login_pass = document.getElementById("login_pass");
    login_pass = login_pass.value;

    let data = {
      email: login_email,
      password: login_pass,
    };
    let res = await fetch("https://blue-blushing-deer.cyclic.app/users/login", {
      method: "POST",
      body: JSON.stringify(data),
      headers: {
        "Content-Type": "application/json",
      },
    });
    let odata = await res.json();
    alert("Login Successful");
    console.log(odata);
    let log_name = document.getElementById("log_name");
    log_name.innerText = "Hello " + login_email;
    localStorage.setItem("hello", login_email);
    localStorage.setItem("token", odata.token);
  } catch (error) {
    console.log(error);
    alert(error);
  }
};

